    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed

        int selectedRowIndex = TbVeiculos.getSelectedRow();
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um veículo para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String placa = TbVeiculos.getValueAt(selectedRowIndex, 0).toString();

        excluirVeiculo(placa);


    }//GEN-LAST:event_btExcluirActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int selectedRowIndex = TbVeiculos.getSelectedRow();
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um veículo para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String placa = TbVeiculos.getValueAt(selectedRowIndex, 0).toString();
        Veiculo veiculo = BancoVeic.getVeiculoByPlaca(placa);

        // Obtenha os outros dados do veículo do banco de dados
        String marca = veiculo.getMarca();
        String modelo = veiculo.getModelo();
        String cor = veiculo.getCor();
        int qtdRodas = veiculo.getQtdRodas();
        int velocMax = veiculo.getVelocMax();
        int qtdPistoes = veiculo.getQtdPistoes();
        int potencia = veiculo.getPotencia();
        String dataCadastro = veiculo.getDataCadastro();
        int qtdePassageiro = veiculo.getQtdPassageiro();

        
        EditarPasseio editarPasseio = new EditarPasseio(placa, marca, modelo, cor, qtdRodas, velocMax, qtdPistoes, potencia, dataCadastro, qtdePassageiro);
        editarPasseio.setVisible(true);
        dispose();
    }//GEN-LAST:event_btAlterarActionPerformed

