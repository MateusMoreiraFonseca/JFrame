    private void listarVeiculosPasseio() {
        DefaultTableModel tabModel = (DefaultTableModel) TbVeiculos.getModel();
        int posLin = 0;
        tabModel.setRowCount(posLin);

        // Assuming you have a collection of VeiculoPasseio objects
        for (Veiculo v : BancoVeic.getBanco().getBdVeiculosPasseio()) {
            tabModel.insertRow(posLin, new Object[]{
                v.getPlaca(),
                v.getMarca(),
                v.getModelo(),
                v.getCor(),
                v.getQtdRodas(),
                v.getVelocMax(),
                v.getQtdPistoes(),
                v.getPotencia(),
                v.getDataCadastro(),
                v.getQtdPassageiro()
            });
            posLin++;
        }
    }

